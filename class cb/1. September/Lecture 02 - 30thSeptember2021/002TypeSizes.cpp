#include<iostream>

using namespace std;

int main() {

	cout << "sizeof(int) = " << sizeof(int) << " Bytes" << endl;
	cout << "sizeof(char) = " << sizeof(char) << " Bytes" << endl;
	cout << "sizeof(float) = " << sizeof(float) << " Bytes" << endl;
	cout << "sizeof(bool) = " << sizeof(bool) << " Bytes" << endl;
	cout << "sizeof(double) = " << sizeof(double) << " Bytes" << endl;
	return 0;
}